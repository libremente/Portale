         <br>
         <h4>1. FINALITA' DEL TRATTAMENTO DEI DATI:</h4>
         il trattamento è diretto all'espletamento da parte del comune di funzioni istituzionali, in virtù di compiti attribuitigli dalla legge e dai regolamenti.<br>
         <br>
         <h4>2. MODALITA' DEL TRATTAMENTO:</h4>
         a) è realizzato attraverso operazioni o complessi di operazioni indicate dall'art. 1 della legge;<br>
         b) è effettuato con l'ausilio di mezzi elettronici o comunque automatizzati;<br>
         c) è svolto dal personale del comune e/o da ditte esterne legate al comune da contratto di appalto di servizi o da incarico d'opera professionale.<br>
         <br>
         <h4>3. CONFERIMENTO DEI DATI:</h4>
         a) può essere obbligatorio ( per obbligo di legge o di regolamento)<br>
         b) può essere necessario (come onere per l'interessato che voglia ottenere un determinato risultato)<br>
         c) può essere facoltativo ( come dato richiesto per rilievi effettuabili dal comune sulla qualità  ed il gradimento di servizi resi alla comunità ).<br>
         <br>
         <h4>4. RIFIUTO DI CONFERIRE I DATI:</h4>
         l'eventuale rifiuto di conferire da parte dell'interessato i dati personali,<br>
         a) nei casi di cui alle lettere a) e b) del punto precedente, non sarà  possibile evadere la pratica od ottenere l'effetto previsto dalla legge o dai regolamenti;<br>
         b) nei casi di cui alla lettera c) del punto precedente, non sarà  possibile utilizzare l'apporto collaborativo del cittadino al fine di migliorare gli uffici ed i servizi.<br>
         <br>
         <h4>5. COMUNICAZIONE DEI DATI:</h4>
         i dati personali acquisiti possono essere fatti oggetto di comunicazione ai seguenti soggetti:<br>
         a) ad altri enti pubblici, nei casi e nei modi previsti dalla legge n. 675/1996;<br>
         b) a soggetti privati ( ditte o consulenti) che abbiano con il comune contratto per l'elaborazione o la catalogazione di detti dati o che abbiano l'incarico di evadere le pratiche relative al cittadino cui si riferiscono i dati stessi, per conto del comune.<br>
         <br>
         <h4>6. DIFFUSIONE DEI DATI:</h4>
         i dati personali non sono soggetti a diffusione.<br>
         <h4>7. TRASFERIMENTO DEI DATI ALL'ESTERO:</h4>
         i dati personali non sono normalmente soggetti a trasferimento all'estero, tranne nel caso in cui una legge espressamente lo preveda.<br>
         <br>
         <h4>8. DIRITTI DELL'INTERESSATO:</h4>
